@app.get("/analyze/{sector}")
def analyze_sector(sector: str):

    if len(sector) < 3:
        raise HTTPException(status_code=400, detail="Invalid sector")

    news = get_news(sector)
    ai_analysis = analyze_with_ai(sector, news)
    report = format_markdown(sector, ai_analysis)

    # 🔹 Save to .md file
    filename = f"{sector}_analysis.md"
    with open(filename, "w", encoding="utf-8") as f:
        f.write(report)

    return {
        "message": f"Report saved as {filename}",
        "report": report
    }